    private void BtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnConfirmarActionPerformed

        
      String estatus = MetodosSQL.SeleccionarUltimaSolicitudAlumno(TablaMovilidades, JTnocontrol.getText());
    if (estatus == null || estatus.equalsIgnoreCase("Cancelado")|| estatus.equalsIgnoreCase("Rechazado")) {
        int respuesta = JOptionPane.showConfirmDialog(null, "¿Deseas Confirmar la solicitud?", "Confirmación de solicitud", JOptionPane.YES_NO_OPTION);
        if (respuesta == JOptionPane.YES_OPTION) {
            // Restricciones de negocio
            if(jTableMaterias.getRowCount()==0){
                JOptionPane.showMessageDialog(null, "Solicitud Incompleta\n\nLlena todos los campos", "Error", JOptionPane.ERROR_MESSAGE);     
           } else if (Integer.parseInt(JTrc.getText()) >= 1) {
                JOptionPane.showMessageDialog(null, "Solicitud Denegada\n\nMotivo: Materias en RC", "Error", JOptionPane.ERROR_MESSAGE);
           } else if (JTadeudos.getText().trim().equalsIgnoreCase("Adeudado")) {
                JOptionPane.showMessageDialog(null, "Solicitud Denegada\n\nMotivo: Se tienen Adeudos", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (Integer.parseInt(jTMovilidades.getText()) >= 3) {
                JOptionPane.showMessageDialog(null, "Solicitud Denegada\n\nMotivo: Numero de Movilidades Superado", "Error", JOptionPane.ERROR_MESSAGE);
            }else{
                MetodosSQL.InsertarMovilidad(JTnocontrol.getText(), (String) JcInstitutos.getSelectedItem());
                MetodosSQL.InsertarMateriasMovilidad(jTableMaterias);
                JcInstitutos.setEnabled(true);
                DefaultTableModel modeloTabla = (DefaultTableModel) jTableMaterias.getModel();
                modeloTabla.setRowCount(0);
                JcMateria.removeAllItems();
                BtnCancelar.setEnabled(false);
            }
        }
    } else {
        if (estatus.equalsIgnoreCase("En Revisión")) {
            JOptionPane.showMessageDialog(null, "Ya tienes una solicitud en proceso");
        } else if (estatus.equalsIgnoreCase("Aceptado")) {
            JOptionPane.showMessageDialog(null, "Ya tienes una solicitud Aceptada");
        }
    }
    }//GEN-LAST:event_BtnConfirmarActionPerformed

    
    
