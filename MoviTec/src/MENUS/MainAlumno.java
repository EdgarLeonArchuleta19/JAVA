    private void BtnEstatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEstatusActionPerformed
        PanelPrincipal.removeAll();
        PanelPrincipal.add(JMovilidades);
        PanelPrincipal.revalidate();
        PanelPrincipal.repaint();  

        DefaultTableModel modeloTabla = (DefaultTableModel) TablaMovilidades.getModel();
        modeloTabla.setRowCount(0);
        MetodosSQL.SeleccionarUltimaSolicitudAlumno(TablaMovilidades, JTnocontrol.getText());

        if (modeloTabla.getRowCount() > 0) {
            MetodosSQL.mostrarMaterias(TablaPlanEstudios, TablaMovilidades);
            TablaPlanEstudios.enable(false);
        } else {
            // Realiza alguna acción si no hay datos en la tabla, por ejemplo:
             JOptionPane.showMessageDialog(null, "No se encontraron solicitudes de movilidad para el alumno.");
        }

String estatus = MetodosSQL.SeleccionarUltimaSolicitudAlumno(TablaMovilidades, JTnocontrol.getText());

if (estatus != null) {
    switch (estatus) {
        case "Aceptado":
            JEstatusSolicitud.setText(estatus);
            JEstatusSolicitud.setForeground(Color.GREEN);
            break;
        case "En Revisión":
            JEstatusSolicitud.setText(estatus);
            JEstatusSolicitud.setForeground(Color.GRAY);
            break;
        case "Rechazado":
            JEstatusSolicitud.setText(estatus);
            JEstatusSolicitud.setForeground(Color.RED);
            break;
        case "Cancelado":
            JEstatusSolicitud.setText(estatus);
            JEstatusSolicitud.setForeground(new Color(128, 0, 128)); // Púrpura
            break;
        default:
            JEstatusSolicitud.setText("");
            JEstatusSolicitud.setForeground(Color.BLACK);
            break;
    }
}else{
    JEstatusSolicitud.setText("");
    JEstatusSolicitud.setForeground(Color.BLACK);
  }
    }//GEN-LAST:event_BtnEstatusActionPerformed

    
    
